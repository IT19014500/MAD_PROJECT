package com.example.rbms;

public class Supplier {
    private String supname;
    private Integer supage;
    private String supemail;
    private Long supcontact;
    private String supnic;

    public Supplier() {
    }

    public String getSupname() {
        return supname;
    }

    public void setSupname(String supname) {
        this.supname = supname;
    }

    public Integer getSupage() {
        return supage;
    }

    public void setSupage(Integer supage) {
        this.supage = supage;
    }

    public String getSupemail() {
        return supemail;
    }

    public void setSupemail(String supemail) {
        this.supemail = supemail;
    }

    public Long getSupcontact() {
        return supcontact;
    }

    public void setSupcontact(Long supcontact) {
        this.supcontact = supcontact;
    }

    public String getSupnic() {
        return supnic;
    }

    public void setSupnic(String supnic) {
        this.supnic = supnic;
    }
}
