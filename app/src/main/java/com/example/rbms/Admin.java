package com.example.rbms;

public class Admin {
    private String aname;
    private int aage;
    private String aemail;
    private Long acn;
    private String anic;
    private String apass;

    public Admin() {
    }

    public String getAname() {
        return aname;
    }

    public void setAname(String aname) {
        this.aname = aname;
    }

    public int getAage() {
        return aage;
    }

    public void setAage(int aage) {
        this.aage = aage;
    }

    public String getAemail() {
        return aemail;
    }

    public void setAemail(String aemail) {
        this.aemail = aemail;
    }

    public Long getAcn() {
        return acn;
    }

    public void setAcn(Long acn) {
        this.acn = acn;
    }

    public String getAnic() {
        return anic;
    }

    public void setAnic(String anic) {
        this.anic = anic;
    }

    public String getApass() {
        return apass;
    }

    public void setApass(String apass) {
        this.apass = apass;
    }
}
